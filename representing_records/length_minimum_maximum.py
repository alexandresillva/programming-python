# The built-in functions len, min, and max can be quite useful.
# len returns the number of elements.
# min and max return the smallest and largest elements of the sequence.
number_list = [1, 25, 2, 8, 6, 26, 32]
print(f'The list has {len(number_list)} elements')
print(f'The largest number is {max(number_list)}')
print(f'The smallest number is {min(number_list)}')
